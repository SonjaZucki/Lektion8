mood = input("Hi! What mood are you in? ")

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Sorry to hear that, but it will get better!")
elif mood == "excited":
    print("Cool! Feels great, right?")
elif mood == "relaxed":
    print("Perfect. Don't let me disturb your peace!")
else:
    print("I don't recognize this mood.")
